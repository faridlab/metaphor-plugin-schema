impl Default for IntegrationTestGenerator {
    fn default() -> Self {
        Self::new()
    }
}

impl Generator for IntegrationTestGenerator {
    fn name(&self) -> &'static str {
        "integration-test"
    }

    fn generate(&self, schema: &ResolvedSchema) -> Result<GeneratedOutput, GenerateError> {
        let mut output = GeneratedOutput::new();

        if schema.schema.models.is_empty() {
            return Ok(output);
        }

        let models: Vec<&Model> = schema.schema.models.iter().collect();
        let module_name = &schema.schema.name;

        // Generate tests/integration/tests/mod.rs
        let tests_mod = self.generate_tests_mod(&models);
        output.add_file(
            PathBuf::from("tests/integration/tests/mod.rs"),
            tests_mod,
        );

        // Generate tests/integration/tests/crud_test_base.rs
        let crud_base = self.generate_crud_test_base();
        output.add_file(
            PathBuf::from("tests/integration/tests/crud_test_base.rs"),
            crud_base,
        );

        // Generate entity-specific tests
        for model in &models {
            let snake_name = to_snake_case(&model.name);
            let entity_test = self.generate_entity_test(model, module_name);
            output.add_file(
                PathBuf::from(format!("tests/integration/tests/{}_api_test.rs", snake_name)),
                entity_test,
            );
        }

        // Generate main integration_tests.rs
        let main_tests = self.generate_integration_tests_main(&models, module_name);
        output.add_file(
            PathBuf::from("tests/integration_tests.rs"),
            main_tests,
        );

        Ok(output)
    }
}
