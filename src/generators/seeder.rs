    /// Resolve the Rust identifier for the host crate that contains the
    /// `seeders/` module, so the seeder binary can `use <crate>::seeders::*`.
    ///
    /// Strategy: read `./Cargo.toml` at generation time and extract
    /// `[package].name` (with `-` → `_`). Falls back to `backbone_<module>`
    /// for the library-style convention used inside `backbone-framework`.
    fn resolve_host_crate_name(module_name: &str) -> String {
        let fallback = || format!("backbone_{}", module_name.replace('-', "_"));
        let Ok(contents) = std::fs::read_to_string("Cargo.toml") else {
            return fallback();
        };
        let mut in_package = false;
        for raw in contents.lines() {
            let line = raw.trim();
            if line.starts_with('[') {
                in_package = line == "[package]";
                continue;
            }
            if !in_package {
                continue;
            }
            // Match: name = "<value>"  (tolerant of whitespace / single-quotes).
            let Some(rest) = line.strip_prefix("name") else { continue };
            let rest = rest.trim_start();
            let Some(rest) = rest.strip_prefix('=') else { continue };
            let rest = rest.trim_start();
            let (open, close) = if rest.starts_with('"') { ('"', '"') } else { ('\'', '\'') };
            if rest.starts_with(open) {
                if let Some(end) = rest[1..].find(close) {
                    let name = &rest[1..1 + end];
                    return name.replace('-', "_");
                }
            }
        }
        fallback()
    }

    /// Generate seeder binary
    fn generate_seeder_binary(&self, models: &[&Model], module_name: &str) -> String {
        let mut output = String::new();
        let crate_name_snake = Self::resolve_host_crate_name(module_name);

        writeln!(output, "//! Database seeder binary for module: {}", module_name).unwrap();
        writeln!(output, "//!").unwrap();
        writeln!(output, "//! Generated by metaphor-schema. Do not edit manually.").unwrap();
        writeln!(output, "//!").unwrap();
        writeln!(output, "//! Run with: cargo run --bin {}-seeder", module_name).unwrap();
        writeln!(output, "//! Or via CLI: backbone seed run --module {}", module_name).unwrap();
        writeln!(output, "//!").unwrap();
        writeln!(output, "//! This binary runs all registered seeders in order.").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "use anyhow::Result;").unwrap();
        writeln!(output, "use sqlx::postgres::PgPoolOptions;").unwrap();
        writeln!(output, "use std::env;").unwrap();
        writeln!(output).unwrap();

        // Import seeders
        writeln!(output, "// Import seeders").unwrap();
        for model in models {
            let pascal_name = &model.name;
            writeln!(output, "use {}::seeders::Seed{}Seeder;", crate_name_snake, pascal_name).unwrap();
        }
        writeln!(output, "use {}::seeders::Seeder;", crate_name_snake).unwrap();
        writeln!(output).unwrap();

        // Main function
        writeln!(output, "#[tokio::main]").unwrap();
        writeln!(output, "async fn main() -> Result<()> {{").unwrap();
        writeln!(output, "    let args: Vec<String> = env::args().collect();").unwrap();
        writeln!(output, "    let force = args.iter().any(|a| a == \"--force\");").unwrap();
        writeln!(output, "    let filter: Option<&str> = args.iter()").unwrap();
        writeln!(output, "        .skip(1)").unwrap();
        writeln!(output, "        .find(|a| !a.starts_with(\"-\"))").unwrap();
        writeln!(output, "        .map(|s| s.as_str());").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "    let database_url = env::var(\"DATABASE_URL\")").unwrap();
        writeln!(output, "        .expect(\"DATABASE_URL must be set\");").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "    println!(\"Connecting to database...\");").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "    let pool = PgPoolOptions::new()").unwrap();
        writeln!(output, "        .max_connections(5)").unwrap();
        writeln!(output, "        .connect(&database_url)").unwrap();
        writeln!(output, "        .await?;").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "    println!(\"Connected to database\");").unwrap();
        writeln!(output, "    println!();").unwrap();
        writeln!(output, "    println!(\"Running seeders for module: {}\");", module_name).unwrap();
        writeln!(output, "    println!();").unwrap();
        writeln!(output).unwrap();

        // Register seeders
        writeln!(output, "    // Register seeders in order").unwrap();
        writeln!(output, "    let mut seeders: Vec<Box<dyn Seeder + Send + Sync>> = Vec::new();").unwrap();
        for model in models {
            let pascal_name = &model.name;
            writeln!(output, "    seeders.push(Box::new(Seed{}Seeder::new()));", pascal_name).unwrap();
        }
        writeln!(output).unwrap();

        // Sort by order
        writeln!(output, "    // Sort by order").unwrap();
        writeln!(output, "    seeders.sort_by_key(|s| s.order());").unwrap();
        writeln!(output).unwrap();

        // Run seeders
        writeln!(output, "    // Run seeders").unwrap();
        writeln!(output, "    for seeder in &seeders {{").unwrap();
        writeln!(output, "        let name = seeder.name();").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "        // Apply filter if specified").unwrap();
        writeln!(output, "        if let Some(f) = filter {{").unwrap();
        writeln!(output, "            if !name.contains(f) {{").unwrap();
        writeln!(output, "                continue;").unwrap();
        writeln!(output, "            }}").unwrap();
        writeln!(output, "        }}").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "        if force {{").unwrap();
        writeln!(output, "            println!(\"   Running {{}}...\", name);").unwrap();
        writeln!(output, "            seeder.run(&pool).await?;").unwrap();
        writeln!(output, "            println!(\"   {{}} completed\", name);").unwrap();
        writeln!(output, "        }} else {{").unwrap();
        writeln!(output, "            println!(\"   Checking {{}}...\", name);").unwrap();
        writeln!(output, "            if seeder.should_run(&pool).await? {{").unwrap();
        writeln!(output, "                println!(\"   Running {{}}...\", name);").unwrap();
        writeln!(output, "                seeder.run(&pool).await?;").unwrap();
        writeln!(output, "                println!(\"   {{}} completed\", name);").unwrap();
        writeln!(output, "            }} else {{").unwrap();
        writeln!(output, "                println!(\"   {{}} already applied, skipping\", name);").unwrap();
        writeln!(output, "            }}").unwrap();
        writeln!(output, "        }}").unwrap();
        writeln!(output, "    }}").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "    println!();").unwrap();
        writeln!(output, "    println!(\"All seeders completed successfully!\");").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "    Ok(())").unwrap();
        writeln!(output, "}}").unwrap();

        output
    }

    /// Generate SQL seed file for an entity
    fn generate_sql_seed(&self, model: &Model) -> String {
        let mut output = String::new();
        let pascal_name = &model.name;
        let snake_name = to_snake_case(&model.name);
        let table_name = model.collection.as_ref()
            .cloned()
            .unwrap_or_else(|| format!("{}s", snake_name));

        writeln!(output, "-- {} Seed Data", pascal_name).unwrap();
        writeln!(output, "-- Generated by metaphor-schema").unwrap();
        writeln!(output, "--").unwrap();
        writeln!(output, "-- This file contains initial seed data for the {} table.", table_name).unwrap();
        writeln!(output, "-- Customize this file to add your initial data.").unwrap();
        writeln!(output).unwrap();

        // Generate insert template based on model fields
        let fields: Vec<&String> = model.fields.iter()
            .filter(|f| {
                // Skip auto-generated fields
                let name = f.name.as_str();
                !["created_at", "updated_at", "deleted_at"].contains(&name) &&
                !f.has_attribute("auto")
            })
            .map(|f| &f.name)
            .collect();

        if !fields.is_empty() {
            writeln!(output, "-- INSERT INTO {} (", table_name).unwrap();
            writeln!(output, "--     {}",
                fields.iter().map(|f| f.as_str()).collect::<Vec<_>>().join(",\n--     ")
            ).unwrap();
            writeln!(output, "-- ) VALUES").unwrap();
            writeln!(output, "-- (").unwrap();
            writeln!(output, "--     -- Add values here").unwrap();
            writeln!(output, "-- );").unwrap();
            writeln!(output).unwrap();
        }

        // Add sample data based on entity type
        self.generate_sample_seed_data(&mut output, model, &table_name);

        output
    }

