//! Seeder generator
//!
//! Generates database seeders for initial and test data.
//! Seeders provide a programmatic way to populate the database with data.
//!
//! ## Seeder Pattern
//!
//! Seeders:
//! - Implement the Seeder trait with name, order, should_run, run, rollback
//! - Can check if data already exists before running
//! - Support rollback for test data cleanup
//! - Are registered in order in the seeder binary
//!
//! ## Generated Output
//!
//! For each module, generates:
//! - `seeders/mod.rs` - Seeder trait and exports
//! - `seeders/{entity}_seeder.rs` - Individual entity seeders
//! - `src/bin/seeder.rs` - Seeder binary runner
//! - `migrations/seeds/{entity}.sql` - SQL seed data files

use super::{GenerateError, GeneratedOutput, Generator};
