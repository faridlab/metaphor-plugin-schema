        writeln!(output, "// ============================================================================").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "/// Trait for database seeders").unwrap();
        writeln!(output, "#[async_trait]").unwrap();
        writeln!(output, "pub trait Seeder {{").unwrap();
        writeln!(output, "    /// Get the name of this seeder").unwrap();
        writeln!(output, "    fn name(&self) -> &'static str;").unwrap();
        writeln!(output).unwrap();
        writeln!(output, "    /// Get the order in which this seeder should run").unwrap();
        writeln!(output, "    fn order(&self) -> i32;").unwrap();
        writeln!(output).unwrap();
        writeln!(output, "    /// Check if this seeder should run (e.g., check if data already exists)").unwrap();
        writeln!(output, "    async fn should_run(&self, pool: &PgPool) -> Result<bool>;").unwrap();
        writeln!(output).unwrap();
        writeln!(output, "    /// Run the seeder").unwrap();
        writeln!(output, "    async fn run(&self, pool: &PgPool) -> Result<()>;").unwrap();
        writeln!(output).unwrap();
        writeln!(output, "    /// Rollback the seeder (optional)").unwrap();
        writeln!(output, "    async fn rollback(&self, _pool: &PgPool) -> Result<()> {{").unwrap();
        writeln!(output, "        Ok(())").unwrap();
        writeln!(output, "    }}").unwrap();
        writeln!(output, "}}").unwrap();
        writeln!(output).unwrap();

        // Seeder type enum
        writeln!(output, "// ============================================================================").unwrap();
        writeln!(output, "// SEEDER TYPE").unwrap();
        writeln!(output, "// ============================================================================").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "/// Type of seeder").unwrap();
        writeln!(output, "#[derive(Debug, Clone, Copy, PartialEq, Eq)]").unwrap();
        writeln!(output, "pub enum SeederType {{").unwrap();
        writeln!(output, "    /// Reference data (required for application to function)").unwrap();
        writeln!(output, "    Reference,").unwrap();
        writeln!(output, "    /// Initial production data").unwrap();
        writeln!(output, "    Production,").unwrap();
        writeln!(output, "    /// Development/testing data").unwrap();
        writeln!(output, "    Development,").unwrap();
        writeln!(output, "    /// Test data (for automated tests)").unwrap();
        writeln!(output, "    Test,").unwrap();
        writeln!(output, "}}").unwrap();
