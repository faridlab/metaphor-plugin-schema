    /// Cycles in the FK graph (e.g. Quotation ↔ Contract) cannot be
    /// inlined per-table without violating creation order. The generator
    /// should defer back-edges to a final `add_deferred_foreign_keys`
    /// migration so the rest of the FKs still inline cleanly.
    #[test]
    fn test_cycle_back_edges_are_deferred() {
        // Quotation has FK to Contract; Contract has FK to Quotation.
        let mk = |name: &str, fk_to: &str, fk_col: &str| {
            let mut m = Model::new(name);
            m.fields = vec![
                Field {
                    name: "id".to_string(),
                    type_ref: TypeRef::Primitive(PrimitiveType::Uuid),
                    attributes: vec![Attribute::new("id")],
                    ..Default::default()
                },
                Field {
                    name: fk_col.to_string(),
                    type_ref: TypeRef::Primitive(PrimitiveType::Uuid),
                    attributes: vec![],
                    ..Default::default()
                },
            ];
            m.relations.push(crate::ast::Relation {
                name: fk_to.to_lowercase(),
                target: TypeRef::Custom(fk_to.to_string()),
                relation_type: RelationType::One,
                attributes: vec![],
                ..Default::default()
            });
            m
        };
        let mut schema = ModuleSchema::new("test");
        schema.models.push(mk("Quotation", "Contract", "contract_id"));
        schema.models.push(mk("Contract", "Quotation", "quotation_id"));
        let resolved = ResolvedSchema { schema };

        let generator = SqlGenerator::new().with_split(true);
        let output = generator.generate(&resolved).unwrap();

        // Exactly one of the two FKs should land in the deferred file.
        let deferred_key = output.files.keys()
            .find(|k| {
                let s = k.to_string_lossy();
                s.contains("add_deferred_foreign_keys") && s.ends_with(".up.sql")
            })
            .expect("expected a deferred FK migration when models form a cycle");
        let deferred = output.files.get(deferred_key).unwrap();
        let inline_count = output.files.iter()
            .filter(|(k, _)| k.to_string_lossy().contains("create_") && k.to_string_lossy().ends_with(".up.sql") && !k.to_string_lossy().contains("deferred"))
            .filter(|(_, v)| v.contains("ADD CONSTRAINT fk_"))
            .count();

        let deferred_count = deferred.matches("ADD CONSTRAINT fk_").count();
        assert_eq!(
            inline_count + deferred_count,
            2,
            "two FKs in total; got inline={} deferred={}",
            inline_count,
            deferred_count,
        );
        assert_eq!(
            deferred_count, 1,
            "one back-edge per 2-cycle should be deferred"
        );

        // The deferred file must have a paired .down.sql with matching DROP CONSTRAINTs.
        let deferred_down_key = output.files.keys()
            .find(|k| {
                let s = k.to_string_lossy();
                s.contains("add_deferred_foreign_keys") && s.ends_with(".down.sql")
            })
            .unwrap();
        let down = output.files.get(deferred_down_key).unwrap();
        assert_eq!(
            down.matches("DROP CONSTRAINT IF EXISTS fk_").count(),
            1,
            "down should drop the same number of constraints"
        );
    }

