#[cfg(test)]
mod tests {
    use super::*;
    use crate::ast::{Attribute, Field, ModuleSchema, PrimitiveType, TypeRef};

    fn create_test_model() -> Model {
        let mut model = Model::new("Order");
        model.fields = vec![
            Field {
                name: "id".to_string(),
                type_ref: TypeRef::Primitive(PrimitiveType::Uuid),
                attributes: vec![Attribute::new("id")],
                ..Default::default()
            },
        ];
        model
    }

    fn create_test_schema() -> ResolvedSchema {
        let mut schema = ModuleSchema::new("test");
        schema.models.push(create_test_model());
        ResolvedSchema { schema }
    }

    #[test]
    fn test_usecase_generator_creates_files() {
        let schema = create_test_schema();
        let generator = UseCaseGenerator::new();
        let output = generator.generate(&schema).unwrap();

        assert!(output.files.contains_key(&PathBuf::from("src/application/usecases/base.rs")));
        assert!(output.files.contains_key(&PathBuf::from("src/application/usecases/order_usecases.rs")));
        assert!(output.files.contains_key(&PathBuf::from("src/application/usecases/mod.rs")));
    }

    #[test]
    fn test_usecase_emits_type_aliases() {
        let schema = create_test_schema();
        let generator = UseCaseGenerator::new();
        let output = generator.generate(&schema).unwrap();

        let content = output.files
            .get(&PathBuf::from("src/application/usecases/order_usecases.rs"))
            .unwrap();

        assert!(content.contains("pub type CreateOrderUseCase = CreateUseCase<"));
        assert!(content.contains("pub type UpdateOrderUseCase = UpdateUseCase<"));
        assert!(content.contains("pub type GetOrderUseCase"));
        assert!(content.contains("pub type DeleteOrderUseCase"));
        assert!(content.contains("pub type ListOrderUseCase"));
        assert!(content.contains("backbone_core"));
    }

    #[test]
    fn test_base_re_exports_backbone_core() {
        let schema = create_test_schema();
        let generator = UseCaseGenerator::new();
        let output = generator.generate(&schema).unwrap();

        let base = output.files
            .get(&PathBuf::from("src/application/usecases/base.rs"))
            .unwrap();

        assert!(base.contains("backbone_core"));
        assert!(base.contains("UseCaseError"));
    }
}
