#[cfg(test)]
mod tests {
    use super::*;
    use crate::ast::{Attribute, Field, ModuleSchema, PrimitiveType, TypeRef};

    fn create_test_model() -> Model {
        let mut model = Model::new("Order");
        model.fields = vec![Field {
            name: "id".to_string(),
            type_ref: TypeRef::Primitive(PrimitiveType::Uuid),
            attributes: vec![Attribute::new("id")],
            ..Default::default()
        }];
        model
    }

    fn create_test_schema() -> ResolvedSchema {
        let mut schema = ModuleSchema::new("test");
        schema.models.push(create_test_model());
        ResolvedSchema { schema }
    }

    #[test]
    fn test_cqrs_creates_files() {
        let schema = create_test_schema();
        let gen = CqrsGenerator::new();
        let out = gen.generate(&schema).unwrap();

        assert!(out.files.contains_key(&PathBuf::from("src/application/commands/order_commands.rs")));
        assert!(out.files.contains_key(&PathBuf::from("src/application/queries/order_queries.rs")));
        assert!(out.files.contains_key(&PathBuf::from("src/application/commands/mod.rs")));
        assert!(out.files.contains_key(&PathBuf::from("src/application/queries/mod.rs")));
    }

    #[test]
    fn test_commands_emits_type_aliases() {
        let schema = create_test_schema();
        let gen = CqrsGenerator::new();
        let out = gen.generate(&schema).unwrap();

        let content = out.files
            .get(&PathBuf::from("src/application/commands/order_commands.rs"))
            .unwrap();

        assert!(content.contains("pub type CreateOrderCommand"));
        assert!(content.contains("pub type UpdateOrderCommand"));
        assert!(content.contains("pub type DeleteOrderCommand"));
        assert!(content.contains("pub type RestoreOrderCommand"));
        assert!(content.contains("pub type OrderCommandHandler"));
        assert!(content.contains("GenericCreateCommand"));
        assert!(content.contains("GenericCommandHandler"));
    }

    #[test]
    fn test_queries_emits_type_aliases() {
        let schema = create_test_schema();
        let gen = CqrsGenerator::new();
        let out = gen.generate(&schema).unwrap();

        let content = out.files
            .get(&PathBuf::from("src/application/queries/order_queries.rs"))
            .unwrap();

        assert!(content.contains("pub type GetOrderQuery"));
        assert!(content.contains("pub type ListOrderQuery"));
        assert!(content.contains("pub type OrderQueryHandler"));
        assert!(content.contains("GenericQueryHandler"));
    }
}
