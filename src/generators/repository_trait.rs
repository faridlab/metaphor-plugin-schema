#[cfg(test)]
mod tests {
    use super::*;
    use crate::ast::{Attribute, Field, ModuleSchema, PrimitiveType};

    fn create_test_model() -> Model {
        let mut model = Model::new("User");
        model.fields = vec![
            Field {
                name: "id".to_string(),
                type_ref: TypeRef::Optional(Box::new(TypeRef::Primitive(PrimitiveType::String))),
                attributes: vec![Attribute::new("id")],
                ..Default::default()
            },
            Field {
                name: "email".to_string(),
                type_ref: TypeRef::Primitive(PrimitiveType::Email),
                attributes: vec![Attribute::new("unique")],
                ..Default::default()
            },
            Field {
                name: "deleted_at".to_string(),
                type_ref: TypeRef::Optional(Box::new(TypeRef::Primitive(PrimitiveType::DateTime))),
                attributes: vec![],
                ..Default::default()
            },
        ];
        model
    }

    fn create_test_schema() -> ResolvedSchema {
        let mut schema = ModuleSchema::new("test");
        schema.models.push(create_test_model());
        ResolvedSchema { schema }
    }

    #[test]
    fn test_repository_trait_generator_creates_files() {
        let schema = create_test_schema();
        let generator = RepositoryTraitGenerator::new();
        let output = generator.generate(&schema).unwrap();

        assert!(output.files.contains_key(&PathBuf::from("src/domain/repositories/user_repository.rs")));
        assert!(output.files.contains_key(&PathBuf::from("src/domain/repositories/mod.rs")));
    }

    #[test]
    fn test_repository_trait_has_crud_methods() {
        let schema = create_test_schema();
        let generator = RepositoryTraitGenerator::new();
        let output = generator.generate(&schema).unwrap();

        let content = output.files.get(&PathBuf::from("src/domain/repositories/user_repository.rs")).unwrap();

        assert!(content.contains("async fn save"));
        assert!(content.contains("async fn find_by_id"));
        assert!(content.contains("async fn find_all"));
        assert!(content.contains("async fn update"));
        assert!(content.contains("async fn delete"));
    }

    #[test]
    fn test_repository_trait_has_soft_delete() {
        let schema = create_test_schema();
        let generator = RepositoryTraitGenerator::new();
        let output = generator.generate(&schema).unwrap();

        let content = output.files.get(&PathBuf::from("src/domain/repositories/user_repository.rs")).unwrap();

        // Should have soft delete methods for model with deleted_at
        assert!(content.contains("async fn soft_delete"));
        assert!(content.contains("async fn restore"));
        assert!(content.contains("async fn empty_trash"));
    }

    #[test]
    fn test_repository_trait_has_unique_field_queries() {
        let schema = create_test_schema();
        let generator = RepositoryTraitGenerator::new();
        let output = generator.generate(&schema).unwrap();

        let content = output.files.get(&PathBuf::from("src/domain/repositories/user_repository.rs")).unwrap();

        // Should have find_by_email for unique field
        assert!(content.contains("async fn find_by_email"));
    }

    #[test]
    fn test_repository_trait_uses_async_trait() {
        let schema = create_test_schema();
        let generator = RepositoryTraitGenerator::new();
        let output = generator.generate(&schema).unwrap();

        let content = output.files.get(&PathBuf::from("src/domain/repositories/user_repository.rs")).unwrap();

        assert!(content.contains("use async_trait::async_trait;"));
        assert!(content.contains("#[async_trait]"));
    }
}
