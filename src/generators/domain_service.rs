#[cfg(test)]
mod tests {
    use super::*;
    use crate::ast::{Attribute, Field, ModuleSchema, PrimitiveType, TypeRef};

    fn create_test_model() -> Model {
        let mut model = Model::new("Order");
        model.fields = vec![Field {
            name: "id".to_string(),
            type_ref: TypeRef::Primitive(PrimitiveType::Uuid),
            attributes: vec![Attribute::new("id")],
            ..Default::default()
        }];
        model
    }

    fn create_test_schema() -> ResolvedSchema {
        let mut schema = ModuleSchema::new("test");
        schema.models.push(create_test_model());
        ResolvedSchema { schema }
    }

    #[test]
    fn test_domain_service_creates_files() {
        let schema = create_test_schema();
        let gen = DomainServiceGenerator::new();
        let out = gen.generate(&schema).unwrap();

        assert!(out.files.contains_key(
            &PathBuf::from("src/domain/services/order_domain_policy.rs")
        ));
        assert!(out.files.contains_key(
            &PathBuf::from("src/domain/services/mod.rs")
        ));
    }

    #[test]
    fn test_no_rules_emits_permit_all_alias() {
        let schema = create_test_schema();
        let gen = DomainServiceGenerator::new();
        let out = gen.generate(&schema).unwrap();

        let content = out.files
            .get(&PathBuf::from("src/domain/services/order_domain_policy.rs"))
            .unwrap();

        assert!(content.contains("PermitAllPolicy"));
        assert!(content.contains("pub type OrderDomainPolicy"));
        assert!(!content.contains("pub struct OrderDomainPolicy"));
    }
}
