        writeln!(output, "/// Trait for snapshot store implementations").unwrap();
        writeln!(output, "#[async_trait]").unwrap();
        writeln!(output, "pub trait SnapshotStore: Send + Sync {{").unwrap();
        writeln!(output, "    /// Save a snapshot").unwrap();
        writeln!(output, "    async fn save(&self, snapshot: &Snapshot) -> Result<()>;").unwrap();
        writeln!(output).unwrap();
        writeln!(output, "    /// Load the latest snapshot for an aggregate").unwrap();
        writeln!(output, "    async fn load(&self, aggregate_id: Uuid) -> Result<Option<Snapshot>>;").unwrap();
        writeln!(output).unwrap();
        writeln!(output, "    /// Delete snapshots for an aggregate").unwrap();
        writeln!(output, "    async fn delete(&self, aggregate_id: Uuid) -> Result<()>;").unwrap();
        writeln!(output, "}}").unwrap();
        writeln!(output).unwrap();

        // PostgreSQL implementation
        writeln!(output, "// ============================================================================").unwrap();
        writeln!(output, "// POSTGRESQL SNAPSHOT STORE").unwrap();
        writeln!(output, "// ============================================================================").unwrap();
        writeln!(output).unwrap();

