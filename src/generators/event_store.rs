    /// Collect all snapshot configurations from event sourced entities
    fn collect_snapshot_configs<'a>(
        &self,
        event_sourced: &'a [EventSourcedConfig],
    ) -> Vec<(&'a String, &'a SnapshotConfig)> {
        event_sourced
            .iter()
            .filter_map(|es| {
                es.snapshot.as_ref().map(|sc| (&es.entity_name, sc))
            })
            .collect()
    }

