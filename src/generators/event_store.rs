        writeln!(output, "pub struct PostgresSnapshotStore {{").unwrap();
        writeln!(output, "    pool: PgPool,").unwrap();
        writeln!(output, "    table_name: String,").unwrap();
        writeln!(output, "}}").unwrap();
        writeln!(output).unwrap();

