//! Event Store generator
//!
//! Generates event store infrastructure for event sourcing.
//! The event store is the persistence mechanism for domain events.
//!
//! ## Event Sourcing
//!
//! Event stores:
//! - Persist all domain events immutably
//! - Support event replay for aggregate reconstruction
//! - Enable temporal queries (state at any point in time)
//! - Provide event streaming for projections
//!
//! ## Generated Output
//!
//! For each module, generates:
//! - `event_envelope.rs` - Event wrapper with metadata
//! - `event_store.rs` - Event store trait and implementation
//! - `snapshot_store.rs` - Snapshot storage for optimization

