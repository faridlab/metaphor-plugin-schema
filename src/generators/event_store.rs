        writeln!(output, "#[derive(Debug, Clone, Serialize, Deserialize, sqlx::FromRow)]").unwrap();
        writeln!(output, "pub struct Snapshot {{").unwrap();
        writeln!(output, "    pub aggregate_id: Uuid,").unwrap();
        writeln!(output, "    pub aggregate_type: String,").unwrap();
        writeln!(output, "    pub version: i64,").unwrap();
        writeln!(output, "    pub state: serde_json::Value,").unwrap();
        writeln!(output, "    pub created_at: DateTime<Utc>,").unwrap();
        writeln!(output, "}}").unwrap();
        writeln!(output).unwrap();

