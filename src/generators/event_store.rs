        writeln!(output, "// <<< CUSTOM SNAPSHOT STORE START >>>").unwrap();
        writeln!(output, "// Add custom snapshot implementations here").unwrap();
        writeln!(output, "// <<< CUSTOM SNAPSHOT STORE END >>>").unwrap();

