        writeln!(output, "    }}").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "    /// Create with custom table name").unwrap();
        writeln!(output, "    pub fn with_table_name(pool: PgPool, table_name: impl Into<String>) -> Self {{").unwrap();
        writeln!(output, "        Self {{").unwrap();
        writeln!(output, "            pool,").unwrap();
        writeln!(output, "            table_name: table_name.into(),").unwrap();
        writeln!(output, "        }}").unwrap();
        writeln!(output, "    }}").unwrap();
        writeln!(output, "}}").unwrap();
