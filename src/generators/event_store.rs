        writeln!(output, "            .bind(snapshot.aggregate_id)").unwrap();
        writeln!(output, "            .bind(&snapshot.aggregate_type)").unwrap();
        writeln!(output, "            .bind(snapshot.version)").unwrap();
        writeln!(output, "            .bind(&snapshot.state)").unwrap();
        writeln!(output, "            .bind(snapshot.created_at)").unwrap();
        writeln!(output, "            .execute(&self.pool)").unwrap();
        writeln!(output, "            .await?;").unwrap();
