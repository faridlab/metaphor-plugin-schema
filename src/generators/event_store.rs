impl Default for EventStoreGenerator {
    fn default() -> Self {
        Self::new()
    }
}

impl Generator for EventStoreGenerator {
    fn generate(&self, schema: &ResolvedSchema) -> Result<GeneratedOutput, GenerateError> {
        let mut output = GeneratedOutput::new();

        if schema.schema.models.is_empty() {
            return Ok(output);
        }

        let models: Vec<&Model> = schema.schema.models.iter().collect();

        // Collect snapshot configs from EventSourcedConfig
        let snapshot_configs = self.collect_snapshot_configs(&schema.schema.event_sourced);

        // Generate event envelope
        output.add_file(
            PathBuf::from("src/infrastructure/event_store/event_envelope.rs"),
            self.generate_event_envelope(&models),
        );

        // Generate event store
        output.add_file(
            PathBuf::from("src/infrastructure/event_store/event_store.rs"),
            self.generate_event_store(&models),
        );

        // Generate snapshot store with snapshot configs from DDD AST
        output.add_file(
            PathBuf::from("src/infrastructure/event_store/snapshot_store.rs"),
            self.generate_snapshot_store(&models, &snapshot_configs),
        );

        // Generate mod.rs
        output.add_file(
            PathBuf::from("src/infrastructure/event_store/mod.rs"),
            self.generate_mod(),
        );

        Ok(output)
    }

    fn name(&self) -> &'static str {
        "event_store"
    }
}

