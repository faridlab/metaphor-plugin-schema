        writeln!(output, "/// | GET | /{} | List with pagination |", model_plural).unwrap();
        writeln!(output, "/// | POST | /{} | Create new |", model_plural).unwrap();
        writeln!(output, "/// | GET | /{}/:id | Get by ID |", model_plural).unwrap();
        writeln!(output, "/// | PUT | /{}/:id | Full update |", model_plural).unwrap();
        writeln!(output, "/// | PATCH | /{}/:id | Partial update |", model_plural).unwrap();
        writeln!(output, "/// | DELETE | /{}/:id | Soft delete |", model_plural).unwrap();
        writeln!(output, "/// | POST | /{}/bulk | Bulk create |", model_plural).unwrap();
        writeln!(output, "/// | POST | /{}/upsert | Upsert |", model_plural).unwrap();
        writeln!(output, "/// | GET | /{}/trash | List deleted |", model_plural).unwrap();
        writeln!(output, "/// | POST | /{}/:id/restore | Restore |", model_plural).unwrap();
        writeln!(output, "/// | DELETE | /{}/empty | Empty trash |", model_plural).unwrap();
