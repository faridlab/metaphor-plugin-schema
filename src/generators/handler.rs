impl Generator for HandlerGenerator {
    fn generate(&self, schema: &ResolvedSchema) -> Result<GeneratedOutput, GenerateError> {
        let mut output = GeneratedOutput::new();

        // Collect model names for parent mod.rs
        let model_names: Vec<String> = schema.schema.models.iter()
            .map(|m| m.name.clone())
            .collect();

        for model in &schema.schema.models {
            // Find matching hook for this model
            let hook = schema
                .schema
                .hooks
                .iter()
                .find(|h| h.model_ref == model.name || h.name == model.name);

            let content = self.generate_handler(model, hook)?;

            if self.group_by_domain {
                // Grouped structure: src/presentation/http/{model}/{model}_handler.rs
                let model_snake = to_snake_case(&model.name);
                let path = build_generated_path(
                    "src/presentation/http",
                    &model.name,
                    &format!("{}_handler.rs", model_snake),
                    true,
                );
                output.add_file(path, content);

                // Create subdirectory mod.rs: src/presentation/http/{model}/mod.rs
                let sub_mod_path = PathBuf::from(format!("src/presentation/http/{}/mod.rs", model_snake));
                let sub_mod_content = build_subdirectory_mod(&model.name, &format!("{}_handler", model_snake));
                output.add_file(sub_mod_path, sub_mod_content);
            } else {
                // Flat structure (original)
                let path = PathBuf::from(format!(
                    "src/presentation/http/{}_handler.rs",
                    to_snake_case(&model.name)
                ));
                output.add_file(path, content);
            }
        }

        // Generate mod.rs for handlers
        let mut mod_content = String::new();
        writeln!(mod_content, "//! HTTP handlers").unwrap();
        writeln!(mod_content, "//!").unwrap();
        writeln!(mod_content, "//! Auto-generated by metaphor-schema. Do not edit manually.").unwrap();
        writeln!(mod_content, "//!").unwrap();
        writeln!(mod_content, "//! These handlers use Axum and backbone-core's BackboneCrudHandler").unwrap();
        writeln!(mod_content, "//! to provide all 12 standard Backbone CRUD endpoints.").unwrap();
        writeln!(mod_content).unwrap();

        if self.group_by_domain {
            // For grouped structure, parent mod.rs declares subdirectories
            writeln!(mod_content, "// Model subdirectories").unwrap();
            for model in &model_names {
                let model_snake = to_snake_case(model);
                writeln!(mod_content, "pub mod {};", model_snake).unwrap();
            }
            writeln!(mod_content).unwrap();
            writeln!(mod_content, "// Re-exports from subdirectories").unwrap();
            for model in &model_names {
                let model_snake = to_snake_case(model);
                writeln!(mod_content, "pub use {}::{{", model_snake).unwrap();
                writeln!(mod_content, "    Create{}Dto,", model).unwrap();
                writeln!(mod_content, "    Update{}Dto,", model).unwrap();
                writeln!(mod_content, "    Patch{}Dto,", model).unwrap();
                writeln!(mod_content, "    {}ResponseDto,", model).unwrap();
                writeln!(mod_content, "    create_{}_routes,", model_snake).unwrap();
                writeln!(mod_content, "    create_{}_read_routes,", model_snake).unwrap();
                writeln!(mod_content, "    create_{}_write_routes,", model_snake).unwrap();
                writeln!(mod_content, "}};").unwrap();
            }
        } else {
            // Flat structure
            for model in &schema.schema.models {
                let model_snake = to_snake_case(&model.name);
                writeln!(mod_content, "pub mod {}_handler;", model_snake).unwrap();
            }
            writeln!(mod_content).unwrap();
            // Custom module declarations (preserved across regeneration)
            writeln!(mod_content, "// <<< CUSTOM").unwrap();
            writeln!(mod_content, "// END CUSTOM").unwrap();
            writeln!(mod_content).unwrap();

            // Re-exports (only route factory functions — services are accessed via module directly)
            writeln!(mod_content, "// Re-exports").unwrap();
            for model in &schema.schema.models {
                let model_snake = to_snake_case(&model.name);
                writeln!(
                    mod_content,
                    "pub use {}_handler::{{create_{}_routes, create_{}_read_routes, create_{}_write_routes}};",
                    model_snake, model_snake, model_snake, model_snake
                ).unwrap();
            }
            // Custom re-exports (preserved across regeneration)
            writeln!(mod_content, "// <<< CUSTOM").unwrap();
            writeln!(mod_content, "// END CUSTOM").unwrap();
        }

        output.add_file(PathBuf::from("src/presentation/http/mod.rs"), mod_content);

        Ok(output)
    }

    fn name(&self) -> &'static str {
        "handler"
    }
}

