        let model_plural = pluralize(&model_snake);

        writeln!(output, "// =============================================================================").unwrap();
        writeln!(output, "// Route Configuration").unwrap();
        writeln!(output, "// =============================================================================").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "/// Create Axum router with all 16 Backbone endpoints for {}.", model.name).unwrap();
        writeln!(output, "///").unwrap();
        writeln!(output, "/// # Routes").unwrap();
        writeln!(output, "///").unwrap();
        writeln!(output, "/// | Method | Path | Description |").unwrap();
