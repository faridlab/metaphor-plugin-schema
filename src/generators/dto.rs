        writeln!(output).unwrap();

        writeln!(output, "impl<T> ApiResponse<T> {{").unwrap();
        writeln!(output, "    pub fn ok(data: T) -> Self {{").unwrap();
        writeln!(output, "        Self {{ success: true, data: Some(data), error: None }}").unwrap();
        writeln!(output, "    }}").unwrap();
        writeln!(output).unwrap();
        writeln!(output, "    pub fn err(code: impl Into<String>, message: impl Into<String>) -> Self {{").unwrap();
