        writeln!(output, "    /// Items per page").unwrap();
        writeln!(output, "    #[serde(default = \"default_per_page\")]").unwrap();
        writeln!(output, "    pub per_page: u32,").unwrap();
        writeln!(output, "    /// Field to sort by").unwrap();
        writeln!(output, "    pub sort_by: Option<String>,").unwrap();
        writeln!(output, "    /// Sort order (asc/desc)").unwrap();
        writeln!(output, "    pub sort_order: Option<String>,").unwrap();
        writeln!(output, "}}").unwrap();
        writeln!(output).unwrap();
        writeln!(output, "fn default_page() -> u32 {{ 1 }}").unwrap();
        writeln!(output, "fn default_per_page() -> u32 {{ 20 }}").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "/// API response wrapper").unwrap();
        writeln!(output, "#[derive(Debug, Clone, Serialize)]").unwrap();
        writeln!(output, "#[cfg_attr(feature = \"openapi\", derive(ToSchema))]").unwrap();
        writeln!(output, "#[serde(rename_all = \"camelCase\")]").unwrap();
        writeln!(output, "pub struct ApiResponse<T> {{").unwrap();
        writeln!(output, "    pub success: bool,").unwrap();
        writeln!(output, "    #[serde(skip_serializing_if = \"Option::is_none\")]").unwrap();
        writeln!(output, "    pub data: Option<T>,").unwrap();
        writeln!(output, "    #[serde(skip_serializing_if = \"Option::is_none\")]").unwrap();
        writeln!(output, "    pub error: Option<ApiError>,").unwrap();
        writeln!(output, "}}").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "#[derive(Debug, Clone, Serialize)]").unwrap();
        writeln!(output, "#[cfg_attr(feature = \"openapi\", derive(ToSchema))]").unwrap();
        writeln!(output, "pub struct ApiError {{").unwrap();
        writeln!(output, "    pub code: String,").unwrap();
        writeln!(output, "    pub message: String,").unwrap();
        writeln!(output, "    #[serde(skip_serializing_if = \"Option::is_none\")]").unwrap();
        writeln!(output, "    pub details: Option<serde_json::Value>,").unwrap();
