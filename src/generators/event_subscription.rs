//! Event Subscription generator
//!
//! Phase 1: Emits a type alias over `GenericEventSubscriber` and a thin registry
//! scaffold, replacing the duplicated handler/registry/registry wiring per module.
//!
//! ## Before (was hundreds of generated lines per module):
//! ```rust,ignore
//! pub struct OrderEventSubscriber { handlers: Vec<...> }
//! impl OrderEventSubscriber { pub async fn dispatch(...) { ... } }
//! pub struct SubscriptionRegistry { ... }
//! ```
//!
//! ## After:
//! ```rust,ignore
//! pub type OrderEventSubscriber = GenericEventSubscriber<OrderEvent>;
//! ```

use super::{GenerateError, GeneratedOutput, Generator};
use crate::resolver::ResolvedSchema;
use std::fmt::Write;
use std::path::PathBuf;

/// Generates event subscription type aliases from schema
pub struct EventSubscriptionGenerator;

impl EventSubscriptionGenerator {
    pub fn new() -> Self {
        Self
    }

    /// Generate the subscriptions/mod.rs entry point.
    fn generate_mod(&self, schema: &ResolvedSchema) -> String {
        let mut output = String::new();
        let module_name = &schema.schema.name;
        let pascal_name = to_pascal_case(module_name);

        writeln!(output, "//! {} Event Subscriptions", pascal_name).unwrap();
        writeln!(output, "//!").unwrap();
        writeln!(output, "//! Generated by metaphor-schema. Do not edit manually.").unwrap();
        writeln!(output, "//!").unwrap();
        writeln!(output, "//! Cross-module event subscriptions. Each entity subscriber is a type alias").unwrap();
        writeln!(output, "//! over `GenericEventSubscriber` from backbone-messaging.").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "pub mod handlers;").unwrap();
        writeln!(output, "pub mod registry;").unwrap();
        writeln!(output).unwrap();
        writeln!(output, "pub use handlers::*;").unwrap();
        writeln!(output, "pub use registry::SubscriptionRegistry;").unwrap();
        writeln!(output).unwrap();

        // Per-entity subscriber type aliases.
        for model in &schema.schema.models {
            let snake = to_snake_case(&model.name);
            let name = &model.name;
            writeln!(output, "pub use handlers::{n}EventSubscriber;", n = name).unwrap();
            let _ = (snake, name); // suppress unused warning
        }

        output
    }

    /// Generate the subscriptions/handlers.rs file.
    ///
    /// Now emits type aliases over `GenericEventSubscriber` rather than
    /// full subscriber structs with duplicated dispatch logic.
    fn generate_handlers(&self, schema: &ResolvedSchema) -> String {
        let mut output = String::new();
        let module_name = &schema.schema.name;
        let pascal_name = to_pascal_case(module_name);

        writeln!(output, "//! Event subscriber type aliases for {} module", pascal_name).unwrap();
        writeln!(output, "//!").unwrap();
        writeln!(output, "//! Generated by metaphor-schema. Do not edit manually.").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "use backbone_messaging::GenericEventSubscriber;").unwrap();
        writeln!(output).unwrap();

        for model in &schema.schema.models {
            let name = &model.name;
            let snake = to_snake_case(name);
            writeln!(output, "use crate::domain::event::{n}Event;", n = name).unwrap();
            let _ = snake; // suppress unused warning
        }
        writeln!(output).unwrap();

        for model in &schema.schema.models {
            let name = &model.name;
            writeln!(output, "/// Subscriber for {} events.", name).unwrap();
            writeln!(output, "///").unwrap();
            writeln!(output, "/// Register handlers via `GenericEventSubscriber::new(topics, handlers)`.").unwrap();
            writeln!(output, "pub type {n}EventSubscriber = GenericEventSubscriber<{n}Event>;",
                n = name).unwrap();
            writeln!(output).unwrap();
        }

        writeln!(output, "// <<< CUSTOM").unwrap();
        writeln!(output, "// END CUSTOM").unwrap();

        output
    }

    /// Generate the subscriptions/registry.rs file.
    fn generate_registry(&self, schema: &ResolvedSchema) -> String {
        let mut output = String::new();
        let module_name = &schema.schema.name;
        let pascal_name = to_pascal_case(module_name);

        writeln!(output, "//! Subscription registry for {} module", pascal_name).unwrap();
        writeln!(output, "//!").unwrap();
        writeln!(output, "//! Generated by metaphor-schema. Do not edit manually.").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "use backbone_messaging::SubscriberRegistry;").unwrap();
        writeln!(output).unwrap();

        writeln!(output, "/// Registry of all event subscriptions for the {} module.", pascal_name).unwrap();
        writeln!(output, "///").unwrap();
        writeln!(output, "/// Register your subscribers with the event bus on startup:").unwrap();
        writeln!(output, "///").unwrap();
        writeln!(output, "/// ```rust,ignore").unwrap();
        writeln!(output, "/// let registry = SubscriptionRegistry::default();").unwrap();
        writeln!(output, "/// registry.register_all(&event_bus).await?;").unwrap();
        writeln!(output, "/// ```").unwrap();
        writeln!(output, "pub type SubscriptionRegistry = SubscriberRegistry;").unwrap();
        writeln!(output).unwrap();
        writeln!(output, "// <<< CUSTOM").unwrap();
        writeln!(output, "// END CUSTOM").unwrap();

        output
    }
}

impl Default for EventSubscriptionGenerator {
    fn default() -> Self {
        Self::new()
    }
}

impl Generator for EventSubscriptionGenerator {
    fn generate(&self, schema: &ResolvedSchema) -> Result<GeneratedOutput, GenerateError> {
        let mut output = GeneratedOutput::new();

        output.add_file(
            PathBuf::from("src/application/subscriptions/mod.rs"),
            self.generate_mod(schema),
        );
        output.add_file(
            PathBuf::from("src/application/subscriptions/handlers.rs"),
            self.generate_handlers(schema),
        );
        output.add_file(
            PathBuf::from("src/application/subscriptions/registry.rs"),
            self.generate_registry(schema),
        );

        Ok(output)
    }

    fn name(&self) -> &'static str {
        "event_subscription"
    }
}

fn to_pascal_case(name: &str) -> String {
    let mut result = String::new();
    let mut capitalize_next = true;
    for c in name.chars() {
        if c == '_' || c == '-' {
            capitalize_next = true;
        } else if capitalize_next {
            result.push(c.to_ascii_uppercase());
            capitalize_next = false;
        } else {
            result.push(c);
        }
    }
    result
}

