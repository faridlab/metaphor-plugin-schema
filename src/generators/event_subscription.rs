#[cfg(test)]
mod tests {
    use super::*;
    use crate::ast::{Attribute, Field, ModuleSchema, PrimitiveType, TypeRef};

    fn create_test_schema() -> ResolvedSchema {
        let mut schema = ModuleSchema::new("bersihir");
        let mut model = crate::ast::Model::new("Order");
        model.fields = vec![Field {
            name: "id".to_string(),
            type_ref: TypeRef::Primitive(PrimitiveType::Uuid),
            attributes: vec![Attribute::new("id")],
            ..Default::default()
        }];
        schema.models.push(model);
        ResolvedSchema { schema }
    }

    #[test]
    fn test_event_subscription_creates_files() {
        let schema = create_test_schema();
        let gen = EventSubscriptionGenerator::new();
        let out = gen.generate(&schema).unwrap();

        assert!(out.files.contains_key(&PathBuf::from("src/application/subscriptions/mod.rs")));
        assert!(out.files.contains_key(&PathBuf::from("src/application/subscriptions/handlers.rs")));
        assert!(out.files.contains_key(&PathBuf::from("src/application/subscriptions/registry.rs")));
    }

    #[test]
    fn test_handlers_emits_type_alias() {
        let schema = create_test_schema();
        let gen = EventSubscriptionGenerator::new();
        let out = gen.generate(&schema).unwrap();

        let content = out.files
            .get(&PathBuf::from("src/application/subscriptions/handlers.rs"))
            .unwrap();

        assert!(content.contains("GenericEventSubscriber"));
        assert!(content.contains("pub type OrderEventSubscriber"));
    }
}
